from random import randint

# Для адаптации кода под любую лотерею можно, объявить константы
MIN_NUM = 1
MAX_NUM = 49
NUM_LOT = 6
def lottery():
    numbers = []
    for i in range(NUM_LOT):
        num = randint(MIN_NUM, MAX_NUM)
        if i in numbers:
            pass
        else:
            numbers.append(num)
    numbers.sort()
    print(f'Номера вашего лотерейного билета = {numbers}')

lottery()